public class OverMethods {
    public static int addingVars (int a, int b) {
        return a + b;
    }
    public static int addingVars (int a, int b, int c) {
        return a + b + c;
    }
    public static double addingVars (double a, double b) {
        return a + b;
    }
    public static String addingVars (String a, String b) {
        return a + b;
    }
}
